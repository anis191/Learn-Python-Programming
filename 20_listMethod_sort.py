# sort(): Sorts the list(by default sort asending)
# Syntax(Asending) --> list_name.sort()
# Desending sort --> list_name.sort(reverse=True)

num = [30,10,50,20,40]
num.sort()
print(num)
num.sort(reverse=True)
print(num)

char = ['z','k','g','p','a']
char.sort()
print(char)
char.sort(reverse=True)
print(char)

name = ["Muhi","Shariar","Anis"] #sort alphabatically ascending
name.sort()
print(name)
name.sort(reverse=True)
print(name)
