# insert(): Adds an element at the specified position
# Syntax --> list_name.insert(index_no, element)
num = [1,2,4,5]
print(num)
num.insert(2, 3)  #insert "3" in 2-no. index
print(num)

Name = ["Anis","Sharia","Muhi"]
print(Name)
Name.insert(0, "Karim")  #insert "Karim" in 0-no. index
print(Name)

info = ["Anis",2314,True]
print(info)
info.insert(2, 3.55)   #insert "3.55" in 2-no. index
print(info)