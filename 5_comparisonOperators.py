'''
Equal --> ==
Not equal --> !=
Greater than --> >
Less than --> <
Greater than or equal to --> >=
Less than or equal to --> <=
'''
a = 8
b = 5
c = 8
print(a == b)
print(a == c)

print(a != b)
print(a != c)

print(a > b)
print(a > c)

print(a < b)
print(b < a)

print(a >= b)
print(b >= c)

print(a <= b)
print(b <= c)
