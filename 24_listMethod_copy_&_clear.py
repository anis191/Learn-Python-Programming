# copy(): Returns a copy of the list
# Syntax --> x = list_name.copy()
num = [1,2,3,4,5]
print(num)
num2 = num.copy()
print(num2)

info = ["Anis",2314,3.44,False]
print(info)
info2 = info.copy()
print(info2)

#clear(): Removes all the elements from the list
num = [1,2,3,4,5]
print(num)
num.clear()
print(num)

info = ["Anis",2314,3.44,False]
print(info)
info.clear()
print(info)