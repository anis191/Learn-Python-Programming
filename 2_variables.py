# Variables syntax --> variable_name = value
# In python there is no semicolon after a statement!
# We can print multiple output/value in a single print() function using comma(,)
'''
 We can see the data type of any variable/data using type() function.
 Syntax --> type(variable)
'''
a = 10
print(a,"Data Type: ",type(a))
b = 7.33
print(b,"Data Type: ",type(b))
c = complex(8,2)
print(c,"Data Type: ",type(c))
d = True #or false
print(d,"Data Type: ",type(d))
e = "Chittagong"
print(e,"Data Type: ",type(e))
