#remove(): Removes the item with the specified value
# Syntax --> list_name.remove(item)
num = [1,2,3,4,5]
print(num)
num.remove(2)
print(num)

Name = ["Anis","Sharia","Muhi"]
print(Name)
Name.remove("Anis")
print(Name)

info = ["Anis",2314,True]
print(info)
info.remove(True)
print(info)

#pop():	Removes the element at the specified position
# Syntax --> list_name.pop(index_no)
num2 = [1,2,3,4,5]
print(num2)
num2.pop(2)
print(num2)

Name2 = ["Anis","Sharia","Muhi"]
print(Name2)
Name2.pop(2)
print(Name2)

info2 = ["Anis",2314,True]
print(info2)
info2.pop(1)
print(info2)