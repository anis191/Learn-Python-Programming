# to integer --> int()
# to float --> float()
# to string --> str()
a = 10.5
print("Data Type of:",a,type(a))
# convert it in int:
a = int(a)
print("Data Type of:",a,type(a))

b = 15
print("Data Type of:",b,type(b))
# convert it in float:
b = float(b)
print("Data Type of:",b,type(b))

c = 25
print("Data Type of:",c,type(c))
# convert it in string:
c = str(c)
print("Data Type of:",c,type(c))